export function useI18n() {
  function setLocale(l) {
    locale.value = l
    localStorage.setItem('cxr-locale', l)
  }

  function t(key) {
    const keys = key.split('.')
    let val = messages[locale.value]
    for (const k of keys) {
      val = val?.[k]
      if (val === undefined) return key
    }
    return val
  }

  return { locale, setLocale, t }
}
